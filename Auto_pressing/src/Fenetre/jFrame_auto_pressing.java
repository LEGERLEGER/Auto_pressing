    private void charge() {
        //creation du modele
        model = new DefaultTableModel();
        //ajout de colonnes au modele
        model.addColumn("NOM");
        model.addColumn("PRENOM");
	model.addColumn("CODE");
        model.addColumn("CONTACT");
        model.addColumn("GENRE");
        //definir le modele par rapport a la table
        List_enreg.setModel(model);
        for (Client c : file.client) {
            //creation d'un objet en ligne
            Object obj[] = {c.getNom(), c.getPrenom(),c.getCode(), c.getContact(),  c.getGenre()};
            model.addRow(obj);
        }
    }
