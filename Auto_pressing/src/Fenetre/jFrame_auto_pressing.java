    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        JFileChooser jc = new JFileChooser();

        //ouverture de la boite en mode selection
        int a = jc.showOpenDialog(this);

        //si l'utilisateur a selectionné un fichier
        if (a == JFileChooser.APPROVE_OPTION) {
            //    recuperation du chemin de fichier
            String chemin = jc.getSelectedFile().getAbsolutePath();
            //creation d'une ImageIcon a partir d'un fichier
            ic = new ImageIcon(chemin);
            //redimensionnement de l'image par rapport a la zone d'affichage
            ic = new ImageIcon(ic.getImage().getScaledInstance(lblImages.getWidth(), lblImages.getHeight(), Image.SCALE_DEFAULT));
            //affichage de l'image dans la zone d'affichage
            lblImages.setIcon(ic);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

