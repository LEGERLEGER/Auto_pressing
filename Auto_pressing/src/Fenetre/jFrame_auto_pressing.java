    private void BtnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAjouterActionPerformed
        //recuperation des valeurs saisies
        String nom = txtNom.getText().trim();
        String prenoms = txtPrenom.getText().trim();
        String genre = cboGenre.getSelectedItem().toString();
        String contact= txtContact.getText().trim();
        //verifier si l'utilisateur a saisi des info
        if (nom.length() == 0) {
            JOptionPane.showMessageDialog(this, "VEUILLEZ SAISIR LE NOM SVP!", "ERROR", JOptionPane.ERROR_MESSAGE);
            //          recuperation du curseur
            txtNom.requestFocus();
        } else if (nom.length() == 0) {
            JOptionPane.showMessageDialog(this, "VEUILLEZ SAISIR LE PRENOM(S) SVP!", "ERROR", JOptionPane.ERROR_MESSAGE);
            //          recuperation du curseur
            txtPrenom.requestFocus();
        } else if (prenoms.length() == 0) {
            JOptionPane.showMessageDialog(this, "VEUILLEZ SAISIR LA DATE DE NAISSANCE SVP!", "ERROR", JOptionPane.ERROR_MESSAGE);

        } else {
            if (ic == null) {
                JOptionPane.showMessageDialog(this, "VEUILLEZ SELECTIONNER UNE IMAGE SVP!", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            } else {
                //creation d'une personne
                Client cli = new Client(nom, prenoms,contact, genre, ic);
                //ajout de la personne a la liste
                file.ajouter(cli);
                //vider les champs
                vider();
                //actualiser la liste
                charge();
                JOptionPane.showMessageDialog(this, "ENREGISTREMENT EFFECTUE AVEC SUCCES!!!", "ENREGISTREMENT", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnAjouterActionPerformed

